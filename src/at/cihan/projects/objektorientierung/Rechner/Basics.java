package at.cihan.projects.objektorientierung.Rechner;

public class Basics {
    public double add(double a, double b){
        return a + b;
    }

    public double sub(double a, double b){
        return a - b;
    }

    public int mul(int a, int b){
        return a * b;
    }

    public int div(int a, int b){
        return a / b;
    }
}
